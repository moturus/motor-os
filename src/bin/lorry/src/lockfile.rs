#![allow(dead_code)]

use std::collections::{BTreeMap, BTreeSet};
use std::fmt::Write as _;
use std::path::Path;

use semver::Version;

use crate::atomic::AtomicFile;
use crate::diagnostic::{Error, Result};
use crate::hash::hex;
use crate::manifest::Manifest;
use crate::resolver::{PackageKey, PackageSourceKey, Resolution, ResolvedEdge, ResolvedSource};

const CRATES_IO_SOURCE: &str = "registry+https://github.com/rust-lang/crates.io-index";

#[derive(Clone, Debug, Eq, Ord, PartialEq, PartialOrd)]
struct Identity {
    name: String,
    version: Version,
    registry: bool,
}

struct Node {
    identity: Identity,
    checksum: Option<[u8; 32]>,
    dependencies: BTreeSet<Identity>,
}

pub fn render(manifest: &Manifest, resolution: &Resolution) -> Result<Vec<u8>> {
    let root_version = Version::parse(&manifest.version.original).map_err(|error| {
        Error::failure(format!(
            "root package has invalid version `{} {}`: {error}",
            manifest.name, manifest.version.original
        ))
    })?;
    let root = Identity {
        name: manifest.name.clone(),
        version: root_version,
        registry: false,
    };
    let mut identities = BTreeMap::new();
    for package in &resolution.packages {
        let identity = identity(package.key.clone());
        if identities
            .insert(package.key.clone(), identity.clone())
            .is_some()
        {
            return Err(Error::failure(format!(
                "resolved graph repeats package `{} {}`",
                package.key.name, package.key.version
            )));
        }
    }

    let mut nodes = BTreeMap::new();
    insert_node(
        &mut nodes,
        Node {
            identity: root,
            checksum: None,
            dependencies: edge_identities(&resolution.root_edges, &identities)?,
        },
    )?;
    for package in &resolution.packages {
        let identity = identities[&package.key].clone();
        let checksum = match (&package.key.source, &package.source) {
            (PackageSourceKey::CratesIo, ResolvedSource::CratesIo { checksum }) => Some(*checksum),
            (PackageSourceKey::Path(key_root), ResolvedSource::Path { logical_root, .. })
                if key_root == logical_root =>
            {
                None
            }
            _ => {
                return Err(Error::failure(format!(
                    "resolved package `{} {}` has inconsistent source identity",
                    package.key.name, package.key.version
                )));
            }
        };
        insert_node(
            &mut nodes,
            Node {
                identity,
                checksum,
                dependencies: edge_identities(&package.lock_edges, &identities)?,
            },
        )?;
    }

    let identities = nodes.keys().cloned().collect::<Vec<_>>();
    let mut output = String::from(
        "# This file is automatically @generated by Cargo.\n\
         # It is not intended for manual editing.\n\
         version = 4\n",
    );
    for node in nodes.values() {
        output.push_str("\n[[package]]\nname = ");
        write_toml_string(&mut output, &node.identity.name);
        output.push_str("\nversion = ");
        write_toml_string(&mut output, &node.identity.version.to_string());
        if node.identity.registry {
            output.push_str("\nsource = ");
            write_toml_string(&mut output, CRATES_IO_SOURCE);
            output.push_str("\nchecksum = ");
            write_toml_string(
                &mut output,
                &hex(&node
                    .checksum
                    .expect("registry lock node must have a checksum")),
            );
        }
        if !node.dependencies.is_empty() {
            output.push_str("\ndependencies = [\n");
            let mut references = node
                .dependencies
                .iter()
                .map(|dependency| dependency_reference(dependency, &identities))
                .collect::<Vec<_>>();
            references.sort_unstable();
            for reference in references {
                output.push(' ');
                write_toml_string(&mut output, &reference);
                output.push_str(",\n");
            }
            output.push(']');
        }
        output.push('\n');
    }
    Ok(output.into_bytes())
}

pub fn write(path: &Path, bytes: &[u8]) -> Result<()> {
    let mut file = AtomicFile::new(path)?;
    file.write_all(bytes)?;
    file.commit()
}

fn identity(key: PackageKey) -> Identity {
    Identity {
        name: key.name,
        version: key.version,
        registry: key.source == PackageSourceKey::CratesIo,
    }
}

fn insert_node(nodes: &mut BTreeMap<Identity, Node>, node: Node) -> Result<()> {
    if nodes.contains_key(&node.identity) {
        return Err(Error::failure(format!(
            "Cargo.lock cannot distinguish multiple {} packages named `{} {}`",
            if node.identity.registry {
                "crates.io"
            } else {
                "path"
            },
            node.identity.name,
            node.identity.version
        )));
    }
    nodes.insert(node.identity.clone(), node);
    Ok(())
}

fn edge_identities(
    edges: &[ResolvedEdge],
    identities: &BTreeMap<PackageKey, Identity>,
) -> Result<BTreeSet<Identity>> {
    edges
        .iter()
        .map(|edge| {
            identities.get(&edge.package).cloned().ok_or_else(|| {
                Error::failure(format!(
                    "resolved lock edge names absent package `{} {}`",
                    edge.package.name, edge.package.version
                ))
            })
        })
        .collect()
}

fn dependency_reference(dependency: &Identity, identities: &[Identity]) -> String {
    let same_name = identities
        .iter()
        .filter(|candidate| candidate.name == dependency.name)
        .collect::<Vec<_>>();
    if same_name.len() == 1 {
        return dependency.name.clone();
    }
    let base = format!("{} {}", dependency.name, dependency.version);
    let same_version = same_name
        .iter()
        .filter(|candidate| candidate.version == dependency.version)
        .count();
    if same_version == 1 || !dependency.registry {
        base
    } else {
        format!("{base} ({CRATES_IO_SOURCE})")
    }
}

fn write_toml_string(output: &mut String, value: &str) {
    output.push('"');
    for character in value.chars() {
        match character {
            '"' => output.push_str("\\\""),
            '\\' => output.push_str("\\\\"),
            '\n' => output.push_str("\\n"),
            '\r' => output.push_str("\\r"),
            '\t' => output.push_str("\\t"),
            character if character.is_control() => {
                let value = character as u32;
                if value <= 0xffff {
                    write!(output, "\\u{value:04X}").unwrap();
                } else {
                    write!(output, "\\U{value:08X}").unwrap();
                }
            }
            character => output.push(character),
        }
    }
    output.push('"');
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::resolver::{FeatureContext, ResolvedPackage};
    use std::fs;
    use std::path::PathBuf;
    use std::sync::atomic::{AtomicU64, Ordering};

    static NEXT_FIXTURE: AtomicU64 = AtomicU64::new(0);

    struct Fixture(PathBuf);

    impl Fixture {
        fn new() -> Self {
            let id = NEXT_FIXTURE.fetch_add(1, Ordering::Relaxed);
            let path = std::env::temp_dir().join(format!("lorry-lock-{}-{id}", std::process::id()));
            let _ = fs::remove_dir_all(&path);
            fs::create_dir_all(path.join("src")).unwrap();
            Self(path)
        }
    }

    impl Drop for Fixture {
        fn drop(&mut self) {
            let _ = fs::remove_dir_all(&self.0);
        }
    }

    fn package(key: PackageKey, source: ResolvedSource) -> ResolvedPackage {
        ResolvedPackage {
            key,
            source,
            local_manifest: None,
            feature_sets: BTreeMap::new(),
            target_features: BTreeSet::new(),
            host_features: BTreeSet::new(),
            edges: Vec::new(),
            lock_edges: Vec::new(),
        }
    }

    #[test]
    fn renders_cargo_v4_path_and_registry_disambiguation_and_round_trips() {
        let fixture = Fixture::new();
        let local_root = fixture.0.join("local");
        let source = "[package]\nname = \"lock-oracle\"\nversion = \"0.1.0\"\n\
                      edition = \"2021\"\n\
                      [dependencies]\n\
                      registry-cfg = { package = \"cfg-if\", version = \"=1.0.4\" }\n\
                      local-cfg = { package = \"cfg-if\", path = \"local\" }\n";
        let manifest = Manifest::parse(&fixture.0, &fixture.0.join("Cargo.toml"), source).unwrap();
        let registry_key = PackageKey {
            name: "cfg-if".to_owned(),
            version: Version::parse("1.0.4").unwrap(),
            source: PackageSourceKey::CratesIo,
        };
        let path_key = PackageKey {
            name: "cfg-if".to_owned(),
            version: Version::parse("1.0.4").unwrap(),
            source: PackageSourceKey::Path(local_root.clone()),
        };
        let packages = vec![
            package(
                path_key.clone(),
                ResolvedSource::Path {
                    logical_root: local_root.clone(),
                    physical_root: local_root,
                    source_tree_sha256: [7; 32],
                    patched_crates_io: false,
                    required_patch: None,
                },
            ),
            package(
                registry_key.clone(),
                ResolvedSource::CratesIo { checksum: [9; 32] },
            ),
        ];
        let resolution = Resolution {
            root_edges: vec![
                ResolvedEdge {
                    dependency_index: 0,
                    alias: "registry-demo".to_owned(),
                    kind: crate::sparse::DependencyKind::Normal,
                    context: FeatureContext::Target(String::new()),
                    package: registry_key,
                },
                ResolvedEdge {
                    dependency_index: 1,
                    alias: "path-demo".to_owned(),
                    kind: crate::sparse::DependencyKind::Normal,
                    context: FeatureContext::Target(String::new()),
                    package: path_key,
                },
            ],
            packages,
        };
        let rendered = render(&manifest, &resolution).unwrap();
        let expected = format!(
            concat!(
                "# This file is automatically @generated by Cargo.\n",
                "# It is not intended for manual editing.\n",
                "version = 4\n\n",
                "[[package]]\n",
                "name = \"cfg-if\"\n",
                "version = \"1.0.4\"\n\n",
                "[[package]]\n",
                "name = \"cfg-if\"\n",
                "version = \"1.0.4\"\n",
                "source = \"{}\"\n",
                "checksum = \"{}\"\n\n",
                "[[package]]\n",
                "name = \"lock-oracle\"\n",
                "version = \"0.1.0\"\n",
                "dependencies = [\n",
                " \"cfg-if 1.0.4\",\n",
                " \"cfg-if 1.0.4 ({})\",\n",
                "]\n",
            ),
            CRATES_IO_SOURCE,
            hex(&[9; 32]),
            CRATES_IO_SOURCE,
        );
        assert_eq!(rendered, expected.as_bytes());

        fs::write(fixture.0.join("Cargo.toml"), source).unwrap();
        fs::write(fixture.0.join("src/lib.rs"), "").unwrap();
        write(&fixture.0.join("Cargo.lock"), &rendered).unwrap();
        let loaded = Manifest::load(&fixture.0).unwrap();
        crate::offline::validate_resolution(&loaded, &resolution).unwrap();
    }
}
